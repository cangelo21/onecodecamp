// Data Types

// Numbers: represents numeric value. e.g. 5, 3, 1.5

let age = 25; //Number
console.log("The value if age is:", age);
typeof(age)
console.log("The type of age is:", typeof(age));

// Strings: textual data enclosed in '' or "", e.g. abcd1234!#$%

let name = "King"; //String
console.log("The value if name is:", name);
console.log("The value of name is:", typeof(name));

let number = '23'; //String also


// Boolean: Either true or false

let isAdmin = false;
console.log("The value of isAdmin is:", isAdmin);
console.log("The type of isAdmin is:", typeof(isAdmin));

// Null: Intentional Absence of value
let car = null;
console.log("The value of car is:", car);
console.log("The type of car is:", typeof(car));


// Undefined: Unintentional absence of value
let city;
console.log("The value of city is:", city)
console.log("The type of city is:", typeof(city));

